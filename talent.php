          <div class="full-text">
            <p>Diplômé du Conservatoire d’art dramatique de Montréal en 1976, Carl Béchard s’est rapidement taillé une place de choix dans le milieu théâtral. Il joue entre autres dans : Woyzeck, Le Misanthrope, L’Asile de la pureté, Merz Variétés, Lulu, Luna-Park, Les Ubs, Cantate Grise, Ubu Cycle et La Trahison orale. Plus récemment, il était de Le Problème avec Moi, Treize à table, Les Fourberies de Scapin, Le Dindon, Oulipo Show, Les Femmes savantes, Combat, La Puce à l’oreille, Le Tour du monde en 80 jours et Monsieur Chasse. Il faut aussi souligner sa collaboration étroite avec Denis Marleau et le Théâtre UBU tout au long de sa carrière théâtrale.</p>
            <p>Carl Béchard a aussi signé plusieurs mises en scène dont Le Malade imaginaire, Toc Toc et L’Emmerdeur. Il a reçu le masque de la Révélation de l’année en 1998 pour deux mises en scène: Ceci n’est pas un Schmürz! et aussi Et Vian! Dans la gueule…qui a d’ailleurs été reprise en 2010 au Théâtre du Nouveau Monde.</p>
            <p>À la télévision, il a joué dans Quatre et demi, Les Bougon, L’Auberge du Chien noir, Il était une fois dans le trouble, La Grande bataille II, Virginie, Belle-Baie et La Galère IV.</p>
            <p>Au cinéma, il incarne des personnages dans les films La Liberté d’une statue, Le Siège de l’âme, La Conciergerie des monstres et L’Odyssée d’Alice Tremblay.</p>
            <p>Carl enseigne aussi depuis plusieurs années au Conservatoire d’art dramatique de Montréal.</p>
